public class Sin_Cos {
	public static void main(String[] args) {
		double sin = 30;
		double cos = 30;
		
		sin = Math.toRadians(sin);
		cos = Math.toRadians(cos);
		
		System.out.println("Sin = " + Math.sin(sin));	
		System.out.println("Cos = " + Math.cos(cos));	
	}

}
